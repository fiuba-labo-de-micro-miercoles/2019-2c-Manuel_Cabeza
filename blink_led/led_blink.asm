.include "m328def.inc"

.CSEG
	SBI DDRB, 5
	RJMP LOOP

	LOOP: 
		SBI PORTB, 5
		CALL DELAY
		CBI PORTB, 5
		CALL DELAY
		RJMP LOOP

DELAY:
	LDI R16,0
	LDI R17,0
	LDI R18,0	

DELAY_LOOP1:
	NOP
	INC R16
	BRNE DELAY_LOOP1
	LDI R16,0
	INC R17
	BRNE DELAY_LOOP1
	LDI R17,200
	INC R18
	BRNE DELAY_LOOP1
	RET

